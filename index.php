		<p>Квадратное уравнение — это уравнение вида Ax2 + Bx + C = 0, где A, B и C — заданные константы (с ограничением A != 0). <br>
		Дискриминант вычисляется как: D = B2-4AC. <br>
		Если D = 0, уравнение имеет единственный действительный корень x = -B/2A, <br>
		в противном случае существует пара корней x1 = (-B+sqrt(D))/2A, x2 = (-B-sqrt(D))/2A </p>
		<?php
		
		function diskr($a, $b, $c)  // переменные a, b. c -константы уравнения, см. выше
		{
		if ($a !=0){				//А  не должно равняться нулю - проверяем условие
		
		$d = $b*$b-4*$a*$c;;   		//вычисляем дискриминант  
        
		if ($d==0) {           		//проверяем условие если Дискриминант равен 0 - тогда единственный корень
            $x1 = $x2 = (-$b)/2*$a;
			echo 'Уравнение имеет единственный корень. x1=x2='.$x1;  //возвращать результат работы таким способом (echo) - плохой тон. Но далее нужно будет вернуть два значения.
        } elseif ($d>0) {			//вычисляем корни уравнения
            $x1 = (-$b+sqrt($d))/(2*$a);
            $x2 = (-$b-sqrt($d))/(2*$a);
			echo 'В результате вычислений уравненя получилось два корня: <br> X1='.$x1.'<br>X2='.$x2;				//Два корня. Выводим результаты вычислений.
			}else{	
				echo'Решения нет, так как Дискриминант отрицательный! ';				//корней нет, т.к. Дискриминант отрицательный (возврат, вывол ошибки)
			}
		}else{
			echo 'Ошибка! По условию квадратного уравнения А не может бытьравно 0!'; 	//выводим ошибку А=0
		}
		}
