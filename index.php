		<?php
		function operlogic($x, $y, $oper)
		{
				switch($oper){
				
				CASE '&&':	//выполняем следующий код если $oper равно &&
				
				if($x&&$y){   	//выполняем соответсвующую логическую операцию
				return '1';		//возвращаем 1 если выражение истинно
				}else{
				return '0';		//возвращаем 0 если выражение ложно
				}
				
				break;
				
				CASE '||':
				if($x||$y){   	//выполняем соответсвующую логическую операцию
				return '1';		//возвращаем 1 если выражение истинно
				}else{
				return '0';		//возвращаем 0 если выражение ложно
				}
				break;
				
				CASE 'xor':
				if($x xor $y){   	//выполняем соответсвующую логическую операцию
				return '1';		//возвращаем 1 если выражение истинно
				}else{
				return '0';		//возвращаем 0 если выражение ложно
				}
				break;
				
				}
				
		}
		
		?>
		
